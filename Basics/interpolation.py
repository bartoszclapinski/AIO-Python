age = 24
print("My age is %d years" % age)

major = "years"
minor = "months"
print("My age is %d %s, %d %s" % (age, major, 6, minor))   # This is called tuple interpolation
print("Pi is approximately %f" % (22 / 7))  # This is called float interpolation
print("Pi is approximately %60.50f" % (22 / 7))  # This is called float interpolation with width and precision

